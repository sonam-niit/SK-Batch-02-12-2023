const person = {
    id:101,
    name:"Sonam Soni",
    age:45
}
console.log(person);
//access values based on key
console.log("Id:",person.id);
console.log("Name:",person.name);
console.log("Age:",person.age);
//access using property name
console.log("Id:",person['id']);
console.log("Name:",person['name']);
console.log("Age:",person['age']);